#include <iostream> // Підключаємо бібліотеку для роботи з ввід/вивід

int main() {
    int min, num, duplicates = 1; // min — поточний мінімум, num — поточне число, duplicates — кількість входжень мінімуму
    std::cin >> min; // Зчитуємо перше число, вважаємо його мінімальним
    // Зчитуємо всі наступні числа до кінця вводу
    while (std::cin >> num) {
        if (num < min) { // Якщо знайдено новий мінімум
            min = num; // Оновлюємо мінімум
            duplicates = 1; // Скидаємо лічильник входжень мінімуму
        }
        else if (num == min) { // Якщо число дорівнює поточному мінімуму
            duplicates++; // Збільшуємо лічильник входжень мінімуму
        }
    }
    // Виводимо True, якщо мінімум зустрівся лише один раз, інакше — False
    std::cout << (duplicates == 1 ? "True" : "False") << '\n';
}
